package com.melot.kktv.domain;

public class CarConfigInfo {
	
    private Integer id;
    
    private String name;
    
    private String icon;
    
    private String photo;
    
    private Integer price;
    
    private Integer monthly;
    
    private Integer monthPrice;
    
    private Integer originalPrice;
    
    private Integer ifLimit;
    
    private Integer hasSold;
    
    private Integer openSold;
    
    private Integer getCondition;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getIcon() {
        return icon;
    }

    public void setIcon(String icon) {
        this.icon = icon;
    }

    public String getPhoto() {
        return photo;
    }

    public void setPhoto(String photo) {
        this.photo = photo;
    }

    public Integer getPrice() {
        return price;
    }

    public void setPrice(Integer price) {
        this.price = price;
    }

    public Integer getMonthly() {
        return monthly;
    }

    public void setMonthly(Integer monthly) {
        this.monthly = monthly;
    }

    public Integer getMonthPrice() {
        return monthPrice;
    }

    public void setMonthPrice(Integer monthPrice) {
        this.monthPrice = monthPrice;
    }

    public Integer getOriginalPrice() {
        return originalPrice;
    }

    public void setOriginalPrice(Integer originalPrice) {
        this.originalPrice = originalPrice;
    }

    public Integer getIfLimit() {
        return ifLimit;
    }

    public void setIfLimit(Integer ifLimit) {
        this.ifLimit = ifLimit;
    }

    public Integer getHasSold() {
        return hasSold;
    }

    public void setHasSold(Integer hasSold) {
        this.hasSold = hasSold;
    }

    public Integer getOpenSold() {
        return openSold;
    }

    public void setOpenSold(Integer openSold) {
        this.openSold = openSold;
    }

    public Integer getGetCondition() {
        return getCondition;
    }

    public void setGetCondition(Integer getCondition) {
        this.getCondition = getCondition;
    }
	
}
