package com.melot.kktv.domain;

public class CarPriceInfo {
	
	private int month;
	private float discount;
	
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public float getDiscount() {
		return discount;
	}
	public void setDiscount(float discount) {
		this.discount = discount;
	}

}
